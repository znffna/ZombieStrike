	inline XMFLOAT4 GetPlaneFromTransform(const XMFLOAT4X4& xmf4Transform)
	{
		// Transform Matrix를 XMMATRIX 형식으로 변환
		XMMATRIX matrix = XMLoadFloat4x4(&xmf4Transform);

		// 평면의 법선 벡터 (Transform의 Z축을 사용 - Look Vector)
		XMVECTOR forward = XMVector3Normalize(matrix.r[2]);

		// 평면의 한 점 (Transform의 위치를 사용)
		XMVECTOR position = matrix.r[3];

		// 법선 벡터의 X, Y, Z를 가져옴
		XMFLOAT3 normal;
		XMStoreFloat3(&normal, forward);

		// 평면 방정식의 D값 계산: -dot(normal, position)
		float d = -XMVectorGetX(XMVector3Dot(forward, position));

		// 결과를 XMFLOAT4로 반환 (a, b, c, d)
		return XMFLOAT4(normal.x, normal.y, normal.z, d);
	}
